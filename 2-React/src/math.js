// Define Pi with lowercase 'pi' (consistent case)
const pi = 3.1415962;

// Define functions using lowercase 'pi'
function doublePi() {
  return pi * 2;
}

function triplePi() {
  // Corrected spelling of 'tripple' to 'triple'
  return pi * 3;
}

// Exporting named exports
export { doublePi, triplePi };

// Exporting 'pi' as the default export
export default pi;
